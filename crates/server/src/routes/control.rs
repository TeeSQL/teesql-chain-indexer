//! `/v1/:chain/clusters/:addr/control/sse` — per-cluster control-plane
//! event stream. Spec docs/specs/control-plane-redesign.md §7, Track
//! D3 + D4 + D5.
//!
//! ## Wire shape
//!
//! Stream-typed SSE frames (`event:` prefix in front of each `data:`
//! payload). Three frame kinds:
//!
//! 1. `event: challenge` — sent first thing after the connection
//!    opens. Payload: `{ "nonce": "0x...", "expires_at": <unix> }`.
//!    Subscriber must respond with the `Authorization` header on a
//!    re-issue of the request OR by passing the signature as a query
//!    parameter on the original request — the spec mandates the
//!    header form, but SSE EventSources cannot set arbitrary headers
//!    on the initial GET. The compromise: the client can either
//!    (a) pre-fetch the challenge via `GET .../control/challenge`
//!    and re-issue the SSE request with `Authorization`, or
//!    (b) pass `?signature=<sig>&expires_at=<exp>&nonce=<nonce>` on
//!    the SSE GET directly. The route accepts either; the validation
//!    path is identical.
//!
//! 2. `event: hole` — emitted when the per-cluster orderer stalls.
//!    Payload: `{ "missing_nonce": <u64>, "highest_buffered": <u64>,
//!    "reason": "buffer_expired" | "buffer_age" | "buffer_full" }`.
//!
//! 3. `event: instruction` — bare control instruction frame. Payload
//!    mirrors the `control_instructions` table row but is filtered
//!    against `targetMembers ∋ subscriber.memberId` (spec §7.1).
//!    Empty `targetMembers` means broadcast to all (spec §5.6).
//!
//! ## Auth handshake
//!
//! Subscriber's `member_id` is captured at handshake. The signature is
//! `k256` over `keccak256("teesql-control-stream-v1" || cluster_addr
//! || member_id || nonce || expires_at)`. Server `ecrecover`s and
//! confirms the recovered address matches the
//! `cluster_members.instance_id` (the on-chain registered TEE-derived
//! signer for that memberId). Mismatch → 401 + close.

