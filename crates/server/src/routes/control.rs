    #[test]
    fn auth_compose_accepts_authorization_header() {
        // member_id + signature via header; nonce + expires_at via query.
        let mid = format!("0x{}", hex::encode([0u8; 32]));
        let sig = format!("0x{}", hex::encode([0u8; 65]));
        let q = ControlSseQuery {
            nonce: Some(format!("0x{}", hex::encode([0u8; 32]))),
            expires_at: Some(unix_now() + 60),
            signature: None,
            member_id: None,
            since_nonce: None,
        };
        let mut h = HeaderMap::new();
        h.insert(
            axum::http::header::AUTHORIZATION,
            format!("TeeSQL-Sig {mid}:{sig}").parse().unwrap(),
        );
        let auth = compose_auth(&q, &h).unwrap();
        assert_eq!(auth.member_id, [0u8; 32]);
        assert_eq!(auth.signature.len(), 65);
    }
